package org.wso2.carbon.message.store.service;

/**
 * Message info instance carries Message information that needed to be viewed at the front End
 */
public class MessageInfo {

    private String soapXml;

    private String messageId;

    public String getMessageId() {
        return messageId;
    }

    public void setMessageId(String messageId) {
        this.messageId = messageId;
    }

    public String getSoapXml() {
        return soapXml;
    }

    public void setSoapXml(String soapXml) {
        this.soapXml = soapXml;
    }
}
